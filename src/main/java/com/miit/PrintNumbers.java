package com.miit;

// to print 1 to 100; will use loops
public class PrintNumbers {

	public static void main(String args[]) {
		for(int i=1;i<=10;i++) {
		System.out.println(i);
		}
		
		int i=0;
		while(i<5) {
			System.out.println(i);	
			i=i+1;
		}
		int j=0;
		do {
			System.out.println(j);
			j=j+1;
		}while(j<5);
	}
}

